n1 = int(input("Enter the first number : "))
n2 = int(input("Enter the second number : "))

#Arithmetic Operators
print(f"n1 + n2 = {n1+n2}")
print(f"n1 - n2 = {n1-n2}")
print(f"n1 * n2 = {n1*n2}")
print(f"n1 / n2 = {n1/n2}")
print(f"n1 // n2 = {n1//n2}")
print(f"n1 % n2 = {n1%n2}")
print(f"n1 ** n2 = {n1**n2}")

#Relational Operators
print(f"n1 == n2 => {n1 == n2}")
print(f"n1 != n2 => {n1 != n2}")
print(f"n1 > n2 => {n1 > n2}")
print(f"n1 < n2 => {n1 < n2}")
print(f"n1 >= n2 => {n1 >= n2}")
print(f"n1 <= n2 => {n1 <= n2}")

#Logical Operators
b1 = bool(input("Enter boolean b1 : "))
b2 = bool(input("Enter boolean b2 : "))
print(f"Logical AND : {b1 and b2}")
print(f"Logical OR : {b1 or b2}")
print(f"Logical NOT b1 : {not b1}")
print(f"Logical NOT b2 : {not b2}")
